            ID_VENDEDOR NUMBER(38) NOT NULL,
            FECHA_PEDIDO DATE NOT NULL,
            PRECIO FLOAT NOT NULL,
            CANTIDAD_DISPOSITIVOS NUMBER(38) NOT NULL,
